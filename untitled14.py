# تحميل الموديل المتخصص في القانون
tokenizer = AutoTokenizer.from_pretrained("nlpaueb/legal-bert-base-uncased")
model = AutoModel.from_pretrained("nlpaueb/legal-bert-base-uncased")

def get_legal_embedding(text):
    # نأخذ جزءاً من النص (أول 512 توكن) لتمثيل الملف
    inputs = tokenizer(text[:2000], return_tensors="pt", truncation=True, padding=True, max_length=512)
    with torch.no_grad():
        outputs = model(**inputs)
    # نأخذ المتوسط لتمثيل النص بالكامل في "متجه" واحد
    return outputs.last_hidden_state.mean(dim=1).squeeze().numpy()

