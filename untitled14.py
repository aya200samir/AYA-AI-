def legal_advisor_engine(new_file_path):
    print(f"🔍 جاري تحليل الملف: {new_file_path}")

    # 1. استخراج النص من الملف الجديد
    new_text = extract_text(new_file_path)

    # 2. فهم المعنى القانوني باستخدام BERT
    new_vector = get_legal_embedding(new_text).reshape(1, -1)

    # 3. تحديد المجموعة (Cluster) التي ينتمي لها الملف
    assigned_cluster = kmeans.predict(new_vector)[0]

    # 4. استخراج الميزات (Feature Extraction)
    has_mfa = 1 if "MFA" in new_text.upper() or "MULTI-FACTOR" in new_text.upper() else 0
    is_children = 1 if "children" in new_text.lower() or "minor" in new_text.lower() else 0

    # 5. التوقع المالي باستخدام XGBoost
    features = np.array([[has_mfa, is_children, assigned_cluster]])
    predicted_fine = model_predictor.predict(features)[0]

    # 6. كتابة التقرير الاستشاري
    print("-" * 30)
    print("📋 التقرير الاستشاري المبدئي (UK GDPR Focus):")
    print(f"📍 المجموعة القانونية المرتبطة: {assigned_cluster}")

    if has_mfa == 1:
        print("⚠️ تنبيه: تم اكتشاف إشارات لضعف في أنظمة التحقق (MFA).")

    if predicted_fine > 0:
        print(f"💰 الغرامة المتوقعة بناءً على سوابق ICO: £{max(0, predicted_fine):,.2f}")
    else:
        print("✅ النظام يرى مخاطر منخفضة بناءً على المعطيات الحالية.")

    print("-" * 30)

