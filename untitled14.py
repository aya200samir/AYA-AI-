def get_legal_explanation(text, predicted_fine):
    explanations = []

    # ربط الكلمات التقنية بالمواد القانونية في UK GDPR
    checks = {
        "MFA": ("Article 32", "فشل في تأمين البيانات (Security of processing) - غياب التحقق الثنائي."),
        "children": ("Article 8", "مخالفة شروط معالجة بيانات الأطفال (Conditions applicable to child's consent)."),
        "transparency": ("Article 12", "نقص في الشفافية في عرض المعلومات للجمهور."),
        "retention": ("Article 5(1)(e)", "الاحتفاظ بالبيانات لفترة أطول من اللازم (Storage limitation)."),
        "cooperation": ("Article 31", "عدم التعاون الكافي مع مكتب مفوض المعلومات (ICO).")
    }

    for key, (article, desc) in checks.items():
        if key.lower() in text.lower():
            explanations.append(f"⚖️ <b>{article}:</b> {desc}")

    if not explanations:
        explanations.append("✅ لم يتم رصد مخالفات صريحة للمواد الأساسية الشائعة.")

    return "<br>".join(explanations)

